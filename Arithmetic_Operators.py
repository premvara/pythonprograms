# Arithmetic Operators in Python
a = 3
b = 2

Summed = a + b
print("The sum is", Summed)

Subtract = a - b
print("Differnce is", Subtract)

Multiplication = a * b
print("The output of multiplications is", Multiplication)

Float_Division = a / b
print("the output of float division is", Float_Division)

Floor_Division = a // b
print("The output of Floor Division is", Floor_Division)

Modulus_Division = a % b
print("The output of Modulus Division is", Modulus_Division)

Power = a ** b
print("Square root is", Power)