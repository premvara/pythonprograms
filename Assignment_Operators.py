# Assignment Operators

a = 3
b = 2

# Assign
print("=== Assign ===")
c = a + b
print("Output of Assign is", c)

# Add and Assign
print("=== Add and Assign ===")
a += b
print("Output of Add and Assign is", a)

# Subtract and Assign
print("=== Subtract and Assign")
a = 3
b = 2
a -= b
print("Output of Subtract and Assign is", a)

# Multiply and Assign
print("=== Multiply and Assign")
a = 3
b = 2
a *= b
print("Output of Multiply and Assign is", a)

# Divide and Assign
print("=== Divide and Assign")
a = 3
b = 2
a /= b
print("Output of Divide and Assign is", a)

# Modulus and Assign
print("=== Modulus and Assign")
a = 3
b = 2
a %= b
print("Output of Modulus and Assign is", a)

# Floor and Assign
print("=== Floor and Assign")
a = 3
b = 2
a //= b
print("Output of Floor and Assign is", a)

# Exponent and Assign
print("=== Exponent and Assign")
a = 3
b = 2
a **= b
print("Output of Exponent and Assign is", a)

# Bitwise AND and Assign
print("=== Bitwise AND and Assign")
a = 3
b = 2
a &= b
print("Output of Bitwise AND and Assign is", a)

# Bitwise OR and Assign
print("=== Bitwise OR and Assign")
a = 3
b = 2
a |= b
print("Output of Bitwise OR and Assign is", a)

# Bitwise XOR and Assign
print("=== Bitwise XOR and Assign")
a = 3
b = 2
a ^= b
print("Output of Bitwise XOR and Assign is", a)







