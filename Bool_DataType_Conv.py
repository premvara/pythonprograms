Bool_Variable = True
print(type(Bool_Variable))
print(Bool_Variable)

# Convert bool to float
bool_float_conv = float(Bool_Variable)
print(type(bool_float_conv))
print(bool_float_conv)

# Convert bool to integer
bool_integ_conv = int(Bool_Variable)
print(type(bool_integ_conv))
print(bool_integ_conv)

# Convert bool to str
bool_str_conv = str(Bool_Variable)
print(type(bool_str_conv))
print(bool_str_conv)