# Comparison or Relational Operators
a = 3
b = 2

Greater_Than = a > b
print("Variable with Greater Value is", Greater_Than)

Lesser_Than = a < b
print("Variable with Lesser Value is", Lesser_Than)

Equal_To = a == b
print("To check if two values are equal is", Equal_To)

Not_Equal_To = a != b
print("Not equal to", Not_Equal_To)

Greater_Than_Or_EqualTo = a >= b
print("Variable with Greater Value or equal to is", Greater_Than_Or_EqualTo)

Lesser_Than_Equal_To = a <= b
print("Variable with Lesser Value or equal to is", Lesser_Than_Equal_To)