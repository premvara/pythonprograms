declare_float = 10.0
print(type(declare_float))

# To convert float to integer
float_integer_conv = int(declare_float)
print(float_integer_conv)
print(type(float_integer_conv))

# To convert float to bool
float_bool_conv = bool(declare_float)
print(type(float_bool_conv))
print(float_bool_conv)

# To convert float to str
float_str_conv = str(declare_float)
print(type(float_str_conv))
print(float_str_conv)
